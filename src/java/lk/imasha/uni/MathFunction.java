package lk.imasha.uni;

public class MathFunction {
    static double add(double num1,double num2) {
        return  num1 + num2;
    }
    static double sub(double num1,double num2) {
        return  num1 - num2;
    }
    static double mul(double num1,double num2) {
        return  num1 * num2;
    }
}
